#include <iostream>
using namespace std;

int main() {
    // Initialize a variable 'i' with the value 1.
    int i = 1;

    // Using a 'for' loop to print numbers from 1 to 5.
    // Loop structure:
    // Initialization: Set 'i' to 1 at the beginning.
    // Condition: Loop continues if 'i' is less than or equal to 5.
    // Update: Increment 'i' by 1 after each iteration.
    for (i = 1; i <= 5; i++) {
        // Loop Body: Output the current value of 'i'
        cout << i << " ";
    }
    

    // Loop Iteration: Loop starts at 1 and continues till 5 (inclusive).

    return 0;
}







